##### Object Referencing #####
#creating oject reference with name temp
temp = 98.5
print(f"temp = {temp}")
new_temp = temp
print(f"new_temp = {new_temp}")
print("============================")
temp = temp + 5.0
print(f"temp = {temp}")
print(f"new_temp = {new_temp}")


